#pragma once

#include <cuda_runtime.h>
#include <stdexcept>
#include <string>

inline void check(cudaError_t status)
{
  if (cudaSuccess != status)
  {
    throw std::runtime_error(std::string("CUDA Error: ") +
                             cudaGetErrorString(status));
  }
}
